#include "libft.h"

void		dfre(char **tabe)
{
	int		i;

	i = 0;
	while (tabe[i])
	{
		ft_strdel(&(tabe[i]));
		i++;
	}
	if (tabe)
	{
		free(tabe);
		tabe = NULL;
	}
}
